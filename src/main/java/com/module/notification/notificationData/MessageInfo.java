package com.module.notification.notificationData;

public class MessageInfo {
	String name;
	String placeholder1;
	String placeholder2;
	String templateName;
	
	MessageInfo(String name, String placeholder1, String placeholder2, String templateName){
		this.name = name;
		this.placeholder1 = placeholder1;
		this.placeholder2 = placeholder2;
		this.templateName = templateName;
	}
}
